    private void txtbuscarprodutoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtbuscarprodutoKeyReleased
        String nome = txtbuscarproduto.getText();
        if(nome.equals("")){
            //JOptionPane.showMessageDialog(this,"Digite Um Produto!");
            txtbuscarproduto.requestFocus();
            PreencherTabela("select * from produto order by idproduto");
        }
        else{
            PreencherTabela("SELECT * FROM produto WHERE produto LIKE '"+nome+ "%'");
        }

    }//GEN-LAST:event_txtbuscarprodutoKeyReleased

