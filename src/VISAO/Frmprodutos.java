    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed

        try {
            BEANproduto obj = new BEANproduto();
            //obj.setIdlogin(Integer.valueOf(txtcodigo.getText()));
            obj.setProduto(txtproduto.getText());
            obj.setValor(txtvalor.getText());
            //obj.setPerfil((String)jComboperfil2.getSelectedItem());
            DAOproduto dao = new DAOproduto();
            dao.cadastrproduto(obj);
            JOptionPane.showMessageDialog(null, "Cadastro Realizado com sucesso!");
            PreencherTabela("SELECT * from produto order by idproduto");
            //PreencherTabela("select * from cliente order by idcliente");
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "ERRO AO CADASTRAR:"+ erro);

        }
    }//GEN-LAST:event_cadastrarActionPerformed

    private void deletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletarActionPerformed
        try {
            BEANproduto obj = new BEANproduto();
            obj.setIdproduto(Integer.parseInt(txtcodigo.getText()));
            DAOproduto dao = new DAOproduto();
            dao.deletaruser(obj);
            JOptionPane.showMessageDialog(null, "Cadastro Deletado com sucesso!");
            PreencherTabela("SELECT * from produto order by idproduto");
            //PreencherTabela("select * from cliente order by idcliente");
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "ERRO AO CADASTRAR:"+ erro);

        }
    }//GEN-LAST:event_deletarActionPerformed

