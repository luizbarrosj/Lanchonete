public void PreencherTabela(String cmdsql){
    
    
    ArrayList dados = new ArrayList();
    String[] Colunas = new String[]{
        "CÃ³digo",
        "Produto",
        "Valor",
    };
    try {
       PreparedStatement stmt = conecta.prepareStatement(cmdsql);
        ResultSet rs = stmt.executeQuery();
       while(rs.next()) {
         dados.add(new Object[]{
             rs.getInt("idproduto"),
             rs.getString("produto"),
             rs.getString("valor"),
             });
       
        }
       rs.first();
       // stmt.execute();
      // 
     
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null,"Erro ao preencher a ArrayList ERRO:"+ e);
    }
    
    Tablecliente modelo = new Tablecliente(dados, Colunas);
    
    tabelafun.setModel(modelo);
    tabelafun.getColumnModel().getColumn(0).setPreferredWidth(308);//id
    tabelafun.getColumnModel().getColumn(0).setResizable(false);//id
    tabelafun.getColumnModel().getColumn(1).setPreferredWidth(300);//nome
    tabelafun.getColumnModel().getColumn(1).setResizable(false);//nome
    tabelafun.getColumnModel().getColumn(2).setPreferredWidth(360);//nasc
    tabelafun.getColumnModel().getColumn(2).setResizable(false);//nasc
    tabelafun.getTableHeader().setResizingAllowed(false);
    tabelafun.setAutoResizeMode(tabelafun.AUTO_RESIZE_ALL_COLUMNS);
    tabelafun.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
   
    
    
}

